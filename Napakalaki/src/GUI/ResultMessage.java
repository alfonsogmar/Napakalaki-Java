    public void setMessage(CombatResult combatResult, String playerName)
    {
        result  = combatResult;
        
        
        switch (combatResult)
        {
            case WIN:
            {
                resultLabel.setText(playerName + " gana el combate");
                break;
            }
            case LOSE:
            {
                resultLabel.setText(playerName + " pierde el combate, ahora debe cumplir el mal rollo");
                break;
            }
            case LOSEANDCONVERT:
            {
                resultLabel.setText(playerName + " pierde el combate y se convierte en un sectario, y tambi√©n debe cumplir el mal rollo");
                break;
            }
            case WINGAME:
            {
                resultLabel.setText(playerName + " gana la partida. Enhorabuena!!!");
                break;
            }
            
            
        }
        
        this.pack();
        this.setVisible(true);
    }

